    public void checkookedBus() {

        String source_to_search = source.getSelectedItem().toString();
        String dest_to_search = destination.getSelectedItem().toString();
        String time_to_search = time.getSelectedItem().toString();

        // for date
        Date today = datePicker.getDate();
        SimpleDateFormat dateFormatter = new SimpleDateFormat("yyyy-MM-dd");
        String date_of_going = dateFormatter.format(today);


        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/test", "root", "");
            Statement st = con.createStatement();

            String qu = "select * from bus where destination='" + dest_to_search + "' and source='" + source_to_search + "' and time='" + time_to_search + "' and date_of_going='" + date_of_going + "'";
            ResultSet rso = (ResultSet) st.executeQuery(qu);
            if (rso.next()) {

                String URL = "src\\image\\bus\\" + rso.getString("name_in_file") + ".JPG";
                //  System.out.println(URL);
                label_image_name = rso.getString("name_in_file");
                Icon icon = new ImageIcon(URL);
                photoLabel.setIcon(icon);
                bus_id_to_store.setText(rso.getString("busId"));
                supervisor_name.setText(rso.getString("supervisor_name"));
                supervisor_mob_no.setText(rso.getString("supervisor_mob_no"));
                taka.setText(rso.getString("per_cost"));


            } else {
                photoLabel.setIcon(null);
                resetAll();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
            resetAll();
        }
    }

    public void resetAll() {
        bus_id_to_store.setText(null);
        supervisor_mob_no.setText(null);
        supervisor_name.setText(null);
        taka.setText(null);
        photoLabel.setIcon(null);
    }

