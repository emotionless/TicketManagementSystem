    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        checkdate ckDate = new checkdate();

        if (source.getSelectedIndex() == destination.getSelectedIndex()) {
            JOptionPane.showMessageDialog(rootPane, "Source and destination name should be different ...");
        } else if (ckDate.isPossible(datePicker.getDate()) == 0) {
            JOptionPane.showMessageDialog(null, "Passed time can not be selected...");
        } else {
            try {

                int ind = time.getSelectedIndex() + 1;
                image_name = source.getSelectedItem().toString() + "_" + destination.getSelectedItem().toString() + ind;
                ImageIO.write(newImage, "jpg", new File("src\\image\\bus\\" + image_name + ".JPG"));
                originalImage = null;
                newImage = null;
                image = true;


                // for date correct format
                Date today = datePicker.getDate();
                SimpleDateFormat dateFormatter = new SimpleDateFormat("yyyy-MM-dd");
                String date_of_going = dateFormatter.format(today);



                ////// ******** It is for database *********///////
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/test", "root", "");
                Statement st = con.createStatement();

                // to delete the previous the record
                String q1 = "delete from bus where busId='" + bus_id_to_store.getText().toString() + "' and destination='" + destination.getSelectedItem().toString() + "' and source='" + source.getSelectedItem().toString() + "' and time='" + time.getSelectedItem().toString() + "' and date_of_going='" + date_of_going + "'";
                st.executeUpdate(q1);


                String query = "insert into bus(busId,name_in_file,destination,time,source,per_cost,supervisor_name,supervisor_mob_no,date_of_going) values(?,?,?,?,?,?,?,?,?)";
                PreparedStatement ps = con.prepareStatement(query);


                ps.setString(1, bus_id_to_store.getText().toString());
                ps.setString(2, image_name);
                ps.setString(3, destination.getSelectedItem().toString());
                ps.setString(4, time.getSelectedItem().toString());
                ps.setString(5, source.getSelectedItem().toString());
                ps.setInt(6, Integer.parseInt(taka.getText().toString()));
                ps.setString(7, supervisor_name.getText());
                ps.setString(8, supervisor_mob_no.getText());
                ps.setString(9, date_of_going);

                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Bus has been selected for " + destination.getSelectedItem() + " root...");
                resetAll();
                st.close();
            } catch (MySQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(null, "Duplicate Bus ID...");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
                resetAll();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

