    public void showBus() {
        dest = destination.getSelectedItem().toString();
        src = source.getSelectedItem().toString();

        // it is for date format
        Date today = datePicker.getDate();
        SimpleDateFormat dateFormatter = new SimpleDateFormat("yyyy-MM-dd");
        String date_of_going = dateFormatter.format(today);

        setNull();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/test", "root", "");
            Statement st = con.createStatement();

            String query = "select * from bus where destination='" + dest + "' and source='" + src + "' and date_of_going='" + date_of_going + "'";
            ResultSet rso = (ResultSet) st.executeQuery(query);

            while (rso.next()) {
                String bus_name = rso.getString("name_in_file");
                String URL = "src\\image\\bus\\" + bus_name + ".JPG";
                Icon icon = new ImageIcon(URL);

                char c = bus_name.charAt(bus_name.length() - 1);

                if (c == '1') {
                    label1.setIcon(icon);
                    label11.setText("Bus Id: " + rso.getString("busId"));
                    Button1.setVisible(true);
                } else if (c == '2') {
                    label2.setIcon(icon);
                    label12.setText("Bus Id: " + rso.getString("busId"));
                    Button2.setVisible(true);
                } else if (c == '3') {
                    label3.setIcon(icon);
                    label13.setText("Bus Id: " + rso.getString("busId"));
                    Button3.setVisible(true);
                } else if (c == '4') {
                    label4.setIcon(icon);
                    label14.setText("Bus Id: " + rso.getString("busId"));
                    Button4.setVisible(true);
                } else if (c == '5') {
                    label5.setIcon(icon);
                    label15.setText("Bus Id: " + rso.getString("busId"));
                    Button5.setVisible(true);
                }

            }
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

