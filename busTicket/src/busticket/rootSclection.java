    public void delete(String time_to_delete) {

        // it is for date format
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure to delete???");
        if (confirm == JOptionPane.YES_OPTION) {
            Date today = datePicker.getDate();
            SimpleDateFormat dateFormatter = new SimpleDateFormat("yyyy-MM-dd");
            String date_of_going = dateFormatter.format(today);

            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/test", "root", "");
                Statement st = con.createStatement();

                String query = "delete from bus where destination='" + destination.getSelectedItem().toString() + "' and source='" + source.getSelectedItem().toString() + "' and date_of_going='" + date_of_going + "' and time='" + time_to_delete + "'";
                int success = st.executeUpdate(query);
                if (success == 1) {
                    showBus();
                }
                st.close();

            } catch (Exception e) {
            }
        }
    }

